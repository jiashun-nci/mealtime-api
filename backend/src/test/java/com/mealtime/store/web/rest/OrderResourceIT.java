        Rider rider;
        if (TestUtil.findAll(em, Rider.class).isEmpty()) {
            rider = RiderResourceIT.createEntity(em);
            em.persist(rider);
            em.flush();
        } else {
            rider = TestUtil.findAll(em, Rider.class).get(0);
        }
        order.setRider(rider);
        // Add required entity
        User user = UserResourceIT.createEntity(em);
